import random

def generarFortuna():
    valorRandom=random.randint(1,5)
    if valorRandom==1:
        print("Cambia de novio")
    elif valorRandom==2:
        print("Estudia mucho mas")
    elif valorRandom==3:
        print("Mas suerte para la proxima")
    elif valorRandom==4:
        print("Maneja con cuidado")
    elif valorRandom==5:
        print("Feliz viaje")